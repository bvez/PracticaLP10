DELIMITER $
CREATE PROCEDURE LISTAR_AEROPUERTOS()
BEGIN
	SELECT CODIGO, NOMBRE FROM AEROPUERTO;
END$
CREATE PROCEDURE MOSTRAR_DATOS_AEROPUERTO(
	IN _CODIGO VARCHAR(10)
)
BEGIN
	SELECT ID_AEROPUERTO, CODIGO, NOMBRE, UBICACION, MTS_ALTURA FROM AEROPUERTO WHERE CODIGO = _CODIGO;
END$
CREATE PROCEDURE REGISTRAR_AEROPUERTO(
      IN _CODIGO VARCHAR(10), IN _NOMBRE VARCHAR(100), IN _UBICACION VARCHAR(50), IN _MTS_ALTURA INT(11)
)
BEGIN
      INSERT INTO AEROPUERTO(CODIGO, NOMBRE, UBICACION, MTS_ALTURA) VALUES (_CODIGO, _NOMBRE, _UBICACION, _MTS_ALTURA);
END$